# アプリケーションタイトル
st.title("タロット占いアプリ")

# ユーザーの名前を入力
name = st.text_input("あなたの名前を入力してください:")

# 占いボタン
if st.button("タロット占いをする"):
    if name:
        # ランダムにタロットカードを引く
        drawn_card = random.choice(list(tarot_deck.keys()))
        card_meaning = tarot_deck[drawn_card]
        
        # 結果を表示
        st.write(f"{name}さん、あなたが引いたタロットカードは「{drawn_card}」です。")
        st.write(f"カードの意味: {card_meaning}")
    else:
        st.warning("名前を入力してください。")

# アプリケーションの説明
st.write("""
このアプリは、タロットカードを引いて占いを行うアプリです。名前を入力して「タロット占いをする」ボタンを押してください。
タロットカードを1枚引いて、その意味に基づいて占い結果をお伝えします。
""")

